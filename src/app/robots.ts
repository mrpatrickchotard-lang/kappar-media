import type { MetadataRoute } from 'next';

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: '*',
        allow: '/',
        disallow: [
          '/admin/',
          '/dashboard/',
          '/api/',
          '/meet/',
          '/book/',
        ],
      },
    ],
    sitemap: 'https://kappar.tv/sitemap.xml',
  };
}
